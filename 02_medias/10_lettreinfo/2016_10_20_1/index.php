<li>&nbsp;<span style="color: #31287c;"><a href="http://www.ensembleici.fr/editorial.nyons.accueillis-et-accueillants-histoire-d-une-mobilisation-dans-le-nyonsais.9568.101.html" target="_blank"><strong>Accueillis et accueillants, histoire d'une mobilisation dans le Nyonsais</strong></a></span></li>
