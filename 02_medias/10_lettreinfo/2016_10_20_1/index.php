</ul>
<p>Le Comit&eacute; Citoyen de Nyons pour les R&eacute;fugi&eacute;s organise une vente aux ench&egrave;res silencieuses d'&oelig;uvres d'art (tableaux, dessins, photos, sculptures...) le dimanche 23 octobre, de 14h &agrave; 17h, &agrave; la Maison de Pays de Nyons</p>
