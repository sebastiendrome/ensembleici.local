<span style="color:#4b0082;">Le fichier PDF dans lequel vous aviez l&#39;habitude de trouver les informations existe toujours, mais avec une petite variante : <strong>en haut des &eacute;v&eacute;nements cliquez sur &quot; l&#39;agenda complet des &eacute;v&eacute;nements au format PDF &quot;</strong> , <strong>en haut des annonces cliquez sur &quot; la liste compl&egrave;te des petites annonces au format PDF &quot;</strong> , vous t&eacute;l&eacute;chargerez ainsi la totalit&eacute; des informations, comme auparavant mais sur 2 fichiers distincts.</span><br />
<br />
Le merci de la semaine : merci aux publieurs ! Merci de nous accorder votre confiance. Nous nous engageons &agrave; rester fid&egrave;le &agrave; l&#39;esprit des petites annonces.<br />
<br />
<em>Note &agrave; l&#39;attention des publieurs :</em> si vous proposez une activit&eacute; &agrave; l&#39;ann&eacute;e, publiez dans la rubrique R&eacute;pertoire. Si vous proposez une ou quelques dates, publiez dans la rubrique Agenda plut&ocirc;t que celle des Annonces (lorsque cela demande inscription, les stages par exemples, cela peut se retrouver dans les deux). N&#39;h&eacute;sitez pas &agrave; nous demander conseil. Nous nous organiserons pour pouvoir bient&ocirc;t facilement rencontrer ceux qui le souhaitent.<br />
<em>Note &agrave; l&#39;attention des habitants des Baronnies :</em> toutes mes excuses pour la faute d&#39;orthographe la semaine derni&egrave;re...<br />
<br />
N&#39;h&eacute;sitez pas &agrave; faire conna&icirc;tre, &agrave; partager,<br />
Avec l&#39;enthousiasme et le plaisir d&#39;&ecirc;tre Ensemble, Ici, dans la Vall&eacute;e de la Dr&ocirc;me / Diois, avec vous !<br />
